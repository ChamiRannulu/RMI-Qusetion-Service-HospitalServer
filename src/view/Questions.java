    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) this.table.getModel();
        int sr = table.getSelectedRow();
        id.setText(dtm.getValueAt(sr, 0).toString());
        txt.setText(dtm.getValueAt(sr, 1).toString());
    }//GEN-LAST:event_tableMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txt.getText().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "Sorry!", "Please enter question", JOptionPane.ERROR_MESSAGE);
        } else {
            Question data = new Question(0, txt.getText());
            try {
                QuestionController.getInstance().Save(data);
                table.removeAll();
                loadTable();
            } catch (Exception ex) {
                Logger.getLogger(Questions.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (0 != Integer.parseInt(id.getText())) {
            Question data = new Question(Integer.parseInt(id.getText()), txt.getText());

            try {
                QuestionController.getInstance().Update(data);
                table.removeAll();
                loadTable();
            } catch (Exception ex) {
                Logger.getLogger(Questions.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Sorry!", "Please select to update!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

