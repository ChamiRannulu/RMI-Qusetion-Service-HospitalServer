    void loadTable() throws Exception {
        List<Question> details = null;

        details = QuestionController.getInstance().SearchAll();

        DefaultTableModel but_dt = (DefaultTableModel) this.table.getModel();
        but_dt.setRowCount(0);

        for (int i = 0; i < details.size(); i++) {
            Vector v = new Vector();

            Question obj = details.get(i);

            v.add(obj.getQuestion_id());
            v.add(obj.getQuestion());

            but_dt.addRow(v);

        }
    }

